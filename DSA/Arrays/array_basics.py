arr = [10, 20, 30, 40, 50]  

# Accessing elements 
print("First Element:", arr[0]) 
print("Middle Element:", arr[2]) 
print("Last Element:", arr[4])  

# Updating 
arr[2] = 300 
print("Updated Array:", arr)  

# Traversal 
print("\nTraversing Array:") 
for item in arr:     
  print(item) 

# Length 
print("\nLength:", len(arr))
